class BlokusGame(Game):
    """
    Blokus adapter for the AlphaZero plug-and-play interface.

    Inspired by RaggedR's Blokus experiments (referenced in RaggedR/nonaga README
    and MCTS_Laboratory PR #107). As noted there, random-rollout MCTS performs
    poorly in Blokus due to the high branching factor (~80-500); this implementation
    uses the neural-network-guided MCTS of AlphaZero, which avoids random rollouts.

    Two-player variant: players alternate, starting from opposite corners.
    The neural network always sees the board from the current player's perspective
    (own pieces = +1, opponent pieces = -1).
    """

    def get_display_name(self) -> str:
        return "Blokus (2-player)"

    def get_action_size(self) -> int:
        return ACTION_SIZE + 1  # +1 for pass action

    def get_init_state(self) -> BlokusState:
        return BlokusState()

    def get_next_state(
        self, state: BlokusState, player: int, action: int
    ) -> Tuple[BlokusState, int]:
        if action == ACTION_SIZE:
            # Pass
            next_state = state.apply_pass(player)
            return next_state, next_state.current_player

        piece_id, var_id, row, col = self._decode_action(action)
        variant = PIECE_VARIANTS[piece_id][var_id]
        cells = [(row + dr, col + dc) for dr, dc in variant]
        next_state = state.apply_placement(player, piece_id, cells)
        return next_state, next_state.current_player

    def get_valid_moves(self, state: BlokusState, player: int) -> List[int]:
        placements = state.get_legal_placements(player)
        actions: List[int] = []
        seen: Set[int] = set()
        for piece_id, var_id, cells in placements:
            row, col = cells[0]  # anchor = first cell (top-left after normalisation)
            action = self._encode_action(piece_id, var_id, row, col)
            if action not in seen:
                seen.add(action)
                actions.append(action)
        if not actions:
            actions.append(ACTION_SIZE)  # pass
        return actions

    def get_game_ended(self, state: BlokusState, player: int) -> float:
        if not state.is_terminal():
            return 0

        score_curr = state.score(player)
        score_opp  = state.score(-player)

        if score_curr < score_opp:
            return 1.0   # current player wins (lower score = better)
        if score_curr > score_opp:
            return -1.0
        return 1e-4       # draw

    def get_canonical_form(self, state: BlokusState, player: int) -> BlokusState:
        """
        Return state from player's perspective.
        If player == -1, swap board values so player's pieces are +1.
        """
        if player == 1:
            return state
        s = state.copy()
        s.board = -state.board  # swap +1/-1
        s.pieces_left = {1: state.pieces_left[-1], -1: state.pieces_left[1]}
        s.first_move = {1: state.first_move[-1], -1: state.first_move[1]}
        s.passes = {1: state.passes[-1], -1: state.passes[1]}
        s.current_player = -state.current_player
        return s

    def get_state_representation(self, state: BlokusState) -> np.ndarray:
        """
        Encode state as (5, 20, 20) tensor.

        Channels:
          0: own pieces (current player = +1)
          1: opponent pieces (= -1 in canonical form)
          2: own available corners (diagonal expansion points)
          3: opponent available corners
          4: own pieces remaining (scalar broadcast across board for piece count)
        """
        planes = np.zeros((5, BOARD_SIZE, BOARD_SIZE), dtype=np.float32)
        planes[0] = (state.board == 1).astype(np.float32)
        planes[1] = (state.board == -1).astype(np.float32)

        # Own corners
        for r, c in state.get_available_corners(1):
            planes[2, r, c] = 1.0

        # Opponent corners
        for r, c in state.get_available_corners(-1):
            planes[3, r, c] = 1.0

        # Remaining piece count (normalised)
        own_remaining = len(state.pieces_left[1]) / NUM_PIECES
        planes[4, :, :] = own_remaining

        return planes

    # ------------------------------------------------------------------
    # Action encoding / decoding
    # ------------------------------------------------------------------

    def action_to_index(self, action: int) -> int:
        return action

    def index_to_action(self, index: int) -> int:
        return index

    @staticmethod
    def _encode_action(piece_id: int, var_id: int, row: int, col: int) -> int:
        return (
            piece_id * MAX_VARIANTS * BOARD_SIZE * BOARD_SIZE
            + var_id * BOARD_SIZE * BOARD_SIZE
            + row * BOARD_SIZE
            + col
        )

    @staticmethod
    def _decode_action(action: int) -> Tuple[int, int, int, int]:
        col      = action % BOARD_SIZE;           action //= BOARD_SIZE
        row      = action % BOARD_SIZE;           action //= BOARD_SIZE
        var_id   = action % MAX_VARIANTS;         action //= MAX_VARIANTS
        piece_id = action
        return piece_id, var_id, row, col
